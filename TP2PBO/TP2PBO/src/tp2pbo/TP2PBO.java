            Connection koneksi  = DriverManager.getConnection("jdbc:mysql://localhost:3306/tp2", "root", ""); //untuk mengkoneksi DB ke driver
            Statement sta = koneksi.createStatement();
            ResultSet resu   = sta.executeQuery(sql);

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
