    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String merk = jTextField1.getText();// untuk hubungin ke text field 
        String plat = jTextField2.getText();// untuk hubungin ke text field
        String warna = jTextField3.getText();// untuk hubungin ke text field
        String jenis = jComboBox1.getSelectedItem().toString();// untuk hubungin ke comboBox
       
        try {                       
                Class.forName("com.mysql.cj.jdbc.Driver");// Menentukan Driver Database
                Connection koneksi  = DriverManager.getConnection("jdbc:mysql://localhost:3306/tp2", "root", ""); //untuk mengkoneksi DB ke driver
                String sql = "INSERT INTO mobil" + "(Merk, Plat, Warna, Jenis) VALUES" + " (?, ?, ?, ?)";           
                PreparedStatement ps = koneksi.prepareStatement(sql);
                ps.setString(1, merk);
                ps.setString(2, plat);
                ps.setString(3, warna);
                ps.setString(4, jenis);
                ps.executeUpdate();
                
                System.out.println("Berhasil");
            
            
           
        }
        catch (Exception e){
            System.out.println("eror");
        }
        jTextField1.setText("");//untuk hubungin ke text field 
        jTextField2.setText("");// untuk hubungin ke text field
        jTextField3.setText("");
        jComboBox1.setSelectedItem(0);
    }//GEN-LAST:event_jButton4ActionPerformed

